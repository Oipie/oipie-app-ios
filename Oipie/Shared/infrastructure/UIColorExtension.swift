import UIKit

extension UIColor {
    class var themeViolet: UIColor {
        return UIColor(named: "Violet")!
    }
    
    class var themeGray: UIColor {
        return UIColor(named: "Gray")!
    }
    
    class var themeWhite: UIColor {
        return UIColor(named: "White")!
    }
}
